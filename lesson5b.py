# functions with parameters 
# example 1 
def greeting (name) :
    print("Hello", name)

# call function 
greeting("Jason")

# example 2
def addition (num1 , num2) :
    answer = num1 + num2
    print("The sum is", answer)

# call function 
addition(65 , 8)

# NB : A function is a reusable block of code that performs a specific task
addition(150 , 900)
addition(800 , 3200)
addition(-1500 , -40)