# python list - it organises related data in an orderly manner 
cars = ["Honda" , "Mazda" , "Surf" , "Volkwagen" , "Chevrolet" , "Nissan GTR"]

# show output
print(cars)

# print item at index 2
print(cars[2])

# append BMW
cars.append("BMW")

# show output
print(cars)

# insert toyota at index 2
cars.insert(2, "Toyota")

# show output
print(cars)

# slicing
# 1. print items from index 2 upto 5
print(cars[2:5])

# 2. print from index 3 and above
print(cars[ 3: ])

# 3. print index four and below
print(cars[: 4])

