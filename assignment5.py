def interest (principal , rate) :
    answer = principal * rate/100
    print("The simple interest is ", answer)

# call function 
interest(20000, 10)

def BMI (weight , height) :
    answer = weight / height ** 2
    print ("Your BMI is", answer)

# call function 
BMI(70 , 1.8)

def area(height , base) :
    answer = 1/2 * base *height
    print("The area of a triangle is", answer)

# call function 
area(20 ,30)