# loop through a list 
languages = ["English" , "Swahili" , "Spanish" , "German" , "French" , "Chinese"]
for language in languages :
    print(language)

countries = ["Kenya" , "Uganda" , "Ethiopia" , "Somalia" , "Rwanda"]
for country in countries:
    print(country)

movies = ["Ted" , "Ted 2" , "Appocalipto" , "Lift" , "Fatherhood"]
for movie in  movies :
    print(movie)

